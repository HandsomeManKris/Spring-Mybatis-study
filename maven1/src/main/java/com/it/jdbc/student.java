package com.it.jdbc;

public class student {
    private int id;
    private String name;
    private stu st;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public stu getSt() {
        return st;
    }

    public void setSt(stu st) {
        this.st = st;
    }
}
